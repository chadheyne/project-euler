#!/usr/bin/env python
from itertools import combinations, product


def is_prime(num):
    if num > 1:
        if num in (2, 3):
            return True
        if not (num % 2 and num % 3):
            return False
        for i in range(3, int(num**0.5) + 1, 2):
            if not num % i:
                return False
        return True
    return False


def large_primes():
    result = 0
    sequences = {str(i): (8, 9)[i not in (0, 2, 8)] for i in range(10)}

    for repeated, replacements in sequences.items():
        for replacement in combinations(range(10), 10 - replacements):
            for digit in product(range(10), repeat=len(replacement)):
                digits = iter(digit)
                number = ''.join([repeated if j not in replacement else str(next(digits)) for j in range(10)])

                if number.startswith('0'):
                    continue

                result = result + int(number) if is_prime(int(number)) else result

    return result


def main():
    answer = large_primes()
    print(answer)


if __name__ == "__main__":
    main()

